// Arrow Functions:
// Arrow function with body:
let newFunc = (a, b) => {
    return a - b;
}
console.log(newFunc(5,1));

// Arrow function wihout body:

let calculate = (a, b) => a * b;
console.log(calculate(2, 3));

// Arrow finction with one parameter:

let divide = a => a / 4;
console.log(divide(36));
